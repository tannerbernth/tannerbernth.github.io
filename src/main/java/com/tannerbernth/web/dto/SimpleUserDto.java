package com.tannerbernth.web.dto;

public class SimpleUserDto {
	Long id;
	String username;

	public SimpleUserDto() {
		super();
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

}